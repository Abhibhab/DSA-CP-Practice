void mergeSort1(int array[], int const begin, int const end)
{
    if (end - begin<1)
        return; // Returns recursively
  
    auto mid = begin + (end - begin) / 2;
    mergeSort1(array, begin, mid);
    mergeSort1(array, mid + 1, end);
    merge(array, begin, mid, end);
}
  



